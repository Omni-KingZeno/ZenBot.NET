    {
        return personalColor switch
        {
            PersonalColor.Red => Color.Red,
            PersonalColor.Blue => Color.Blue,
            PersonalColor.Yellow => Color.Gold,
            PersonalColor.Green => Color.Green,
            PersonalColor.Black => new Color(0x000000),
            PersonalColor.Brown => new Color(0x80461B),
            PersonalColor.Purple => Color.Purple,
            PersonalColor.Gray => Color.LightGrey,
            PersonalColor.White => new Color(0xFFFFFF),
            PersonalColor.Pink => Color.Magenta,
            _ => Color.Default,
        };
    }
